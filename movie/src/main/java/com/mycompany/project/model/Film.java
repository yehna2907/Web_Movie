package com.mycompany.project.model;

public class Film {
	private int fno;
	private String ftitle;
	private String fsummary;
	private int fagelimit;
	private double fscore;
	private String fdirector;
	private String frunningtime;
	private String fgenre;
	
	public int getFno() {
		return fno;
	}
	public void setFno(int fno) {
		this.fno = fno;
	}
	public String getFtitle() {
		return ftitle;
	}
	public void setFtitle(String ftitle) {
		this.ftitle = ftitle;
	}
	public String getFsummary() {
		return fsummary;
	}
	public void setFsummary(String fsummary) {
		this.fsummary = fsummary;
	}
	public int getFagelimit() {
		return fagelimit;
	}
	public void setFagelimit(int fagelimit) {
		this.fagelimit = fagelimit;
	}
	public double getFscore() {
		return fscore;
	}
	public void setFscore(double fscore) {
		this.fscore = fscore;
	}
	public String getFdirector() {
		return fdirector;
	}
	public void setFdirector(String fdirector) {
		this.fdirector = fdirector;
	}
	public String getFrunningtime() {
		return frunningtime;
	}
	public void setFrunningtime(String frunningtime) {
		this.frunningtime = frunningtime;
	}
	public String getFgenre() {
		return fgenre;
	}
	public void setFgenre(String fgenre) {
		this.fgenre = fgenre;
	}
	
}
